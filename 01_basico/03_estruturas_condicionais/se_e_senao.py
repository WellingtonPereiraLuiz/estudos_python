#Verifica se a pessoa e de maior
idade = int(input("Digite sua idade: "))
if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")


#Verificar numeros par ou impar
numero = int(input("Digite um número: "))

if numero % 2 ==0:
    print("Numero Par")
else:
    print("Numero Impar")