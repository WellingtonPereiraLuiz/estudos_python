#Uma simples soma de dois dados.
num1 = int(input("Digite o primeiro número:"))
num2 = int(input("Digite o segundo número:"))
soma = num1 + num2
print(f"A soma entre {num1} e {num2} é igual a: {soma}")

#A diferença entre dois dados.
diferenca = num1 - num2
print(f"A diferença entre {num1} e {num2} é igual a: {diferenca}")

#O resto da divisão entre dois dados.
resto_da_divisao = num1 % num2
print(f"O resto da divisão entre {num1} e {num2} é igual a: {resto_da_divisao}")
