# Operadores Aritméticos
a = 10
b = 5
soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b
modulo = a % b
exponenciacao = a ** b
divisao_inteira = a // b
print(f"Soma: {soma}")
print(f"Subtração: {subtracao}")
print(f"Multiplicação: {multiplicacao}")
print(f"Divisão: {divisao}")
print(f"Módulo: {modulo}")
print(f"Exponenciação: {exponenciacao}")
print(f"Divisão Inteira: {divisao_inteira}")

# Operadores de Comparação
maior = a > b
menor = a < b
igual = a == b
diferente = a != b
maior_ou_igual = a >= b
menor_ou_igual = a <= b
print(f"Maior: {maior}")
print(f"Menor: {menor}")
print(f"Igual: {igual}")
print(f"Diferente: {diferente}")
print(f"Maior ou Igual: {maior_ou_igual}")
print(f"Menor ou Igual: {menor_ou_igual}")

# Operadores Lógicos
c = True
d = False
and_logico = c and d
or_logico = c or d
not_logico = not c
print(f"AND Lógico: {and_logico}")
print(f"OR Lógico: {or_logico}")
print(f"NOT Lógico: {not_logico}")

# Operadores de Atribuição
x = 5
x += 2  # x = x + 2
print(f"Atribuição com Adição: {x}")
x -= 3  # x = x - 3
print(f"Atribuição com Subtração: {x}")
x *= 2  # x = x * 2
print(f"Atribuição com Multiplicação: {x}")
x /= 2  # x = x / 2
print(f"Atribuição com Divisão: {x}")
x %= 3  # x = x % 3
print(f"Atribuição com Módulo: {x}")
x **= 2  # x = x ** 2
print(f"Atribuição com Exponenciação: {x}")
x //= 2  # x = x // 2
print(f"Atribuição com Divisão Inteira: {x}")


